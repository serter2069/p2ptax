async function main() {
  console.log("Seeding extended FNS catalog…");

  // Upsert cities first.
  const cityIds: Record<string, string> = {};
  for (const c of CITIES) {
    if (cityIds[c.slug]) continue; // skip dup-slug entries (kursk*)
    const city = await prisma.city.upsert({
      where: { slug: c.slug },
      update: { name: c.name },
      create: c,
    });
    cityIds[c.slug] = city.id;
  }
  console.log(`Cities: ${Object.keys(cityIds).length}`);

  // Upsert FNS, building a search aliases string from name + code + city.
  let added = 0;
  let updated = 0;
  for (const fns of FNS) {
    const cityId = cityIds[fns.citySlug];
    if (!cityId) {
      console.warn(`skip ${fns.code}: city ${fns.citySlug} not found`);
      continue;
    }
    const cityName = CITIES.find((c) => c.slug === fns.citySlug)?.name ?? "";
    const aliases = [
      fns.name.toLowerCase(),
      fns.code,
      cityName.toLowerCase(),
      "ифнс фнс налоговая инспекция",
    ].join(" ");

    const existed = await prisma.fnsOffice.findUnique({
      where: { code: fns.code },
      select: { id: true },
    });
    await prisma.fnsOffice.upsert({
      where: { code: fns.code },
      update: {
        name: fns.name,
        cityId,
        address: fns.address ?? null,
        searchAliases: fns.searchAliases ?? aliases,
      },
      create: {
        code: fns.code,
        name: fns.name,
        cityId,
        address: fns.address ?? null,
        searchAliases: fns.searchAliases ?? aliases,
      },
    });
    if (existed) updated++;
    else added++;
  }

  const total = await prisma.fnsOffice.count();
  console.log(`Added: ${added}, updated: ${updated}, total now: ${total}`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
