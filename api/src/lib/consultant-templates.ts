export function findTemplate(id: string): ConsultantTemplate | undefined {
  return TEMPLATES.find((t) => t.id === id);
}

/**
 * Поиск релевантных шаблонов по тексту вопроса+ответа.
 * Возвращает максимум 2 наиболее вероятных шаблона.
 */
export function suggestTemplatesFor(text: string): Array<{ id: string; label: string }> {
  const matches = TEMPLATES.filter((t) => t.keywords.test(text));
  return matches.slice(0, 2).map((t) => ({ id: t.id, label: t.label }));
}
